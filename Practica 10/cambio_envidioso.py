def cambio_greedy(cantidad, denominaciones):
    denominaciones = sorted(denominaciones, reverse=True)  # Denominaciones deben estar ordenadas de mayor a menor
    resultado = []  # aqu√≠ debe aparecerme el tipo de moneda post cuantas veces me dan ese tipo de moneda [50,1]

    for moneda in denominaciones:
        if cantidad <= 0:
            break

        if cantidad >= moneda:
            num = cantidad // moneda
            cantidad = cantidad - num * moneda
            resultado.append([moneda, num])

    if cantidad > 0:
        print("No te puedo dar un cambio exacto con las denominaciones que me diste.")

    return resultado


